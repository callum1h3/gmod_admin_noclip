noclipcamera = noclipcamera or {}

-- Camera Reminder:

noclipcamera.camerareminder = true

-- Camera Configuration: 

noclipcamera.cameraupdate = 0.5 -- This will update the cameras position, if the server is experiencing lag then turn this up.
noclipcamera.cameraspeed = 100 -- This is the basic speed of the camera when moving.
noclipcamera.cameraspeedboost = 5 -- This is the speed mutliply when holding down shift.
noclipcamera.bringplayer = false -- This will teleport the user when existing admin noclip.
noclipcamera.adminranks = {"superadmin"} -- These are the ranks that are allowed to use the tool.

-- Camera Communication:

noclipcamera.voicechatrange = 550 -- The range of voice chat.
noclipcamera.textchatrange = 300 -- The range of normal chat.
noclipcamera.voice3d = true -- Set to true if you want the voices to fade out the further you get.

-- Text Colors:

noclipcamera.playernamecolor = Color( 100, 100, 255 ) -- The name of the player will display in this color.
noclipcamera.playertextcolor = Color( 255, 255, 255 ) -- The text of the chat will display in this color.




